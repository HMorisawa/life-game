def update(state,height,width):
    state2=np.zeros((height,width))
    dx=[1,1,1,0,0,-1,-1,-1]
    dy=[1,0,-1,1,-1,1,0,-1]
    for i in range(height):
        for j in range(width):
            count=0
            for k in range(8):
                count+=state[(i+dx[k])%height][(j+dy[k])%width]
            if(state[i][j]==1):
                if(count==3 or count==2):
                    state2[i][j]=1
            else:
                if(count==3):
                    state2[i][j]=1
    return state2

